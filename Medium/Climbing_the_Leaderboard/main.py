# Complete the climbingLeaderboard function below.
def climbingLeaderboard(scores, alice):
    scores = sorted(list(dict.fromkeys(scores)))
    pos = 0
    tmp = []
    for sc in alice:
        while (len(scores) > pos and sc >= scores[pos]):
            pos += 1
        tmp.append(len(scores)+1-pos) 
    return tmp

